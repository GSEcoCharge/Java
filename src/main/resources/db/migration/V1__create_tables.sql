

Create Table USUARIO (
  USUARIO_ID integer primary key,
  NOME varchar(100),
  EMAIL varchar(100),
  SENHA varchar(100),
  IMG_PERFIL blob,
  DATA_CRIACAO date,
  ULTIMA_LOCALIZACAO varchar(50)
);

Create Table VEICULO (
  VEICULO_ID integer primary key,
  USUARIO_ID integer,
  MARCA varchar(50),
  MODELO varchar(50),
  ANO integer,
  AUTONOMIA integer,
  TIPO_CONECTOR varchar(50),
  foreign key (USUARIO_ID) references USUARIO(USUARIO_ID)
);

Create Table POSTO_CARREGAMENTO (
  POSTO_ID integer primary key,
  NOME varchar(100),
  LATITUDE decimal(10, 8),
  LONGITUDE decimal(11, 8),
  ENDERECO varchar(255),
  HORARIO_FUNCIONAMENTO varchar(100),
  FORMAS_PAGAMENTO varchar(100),
  AVALIACAO_MEDIA decimal(3, 2)
);

Create Table PONTO_CARREGAMENTO (
  PONTO_ID integer primary key,
  POSTO_ID integer,
  TIPO_CONECTOR varchar(50),
  VELOCIDADE_CARREGAMENTO integer,
  DISPONIBILIDADE varchar(50),
  RESERVAVEL char(1),
  foreign key (POSTO_ID) references POSTO_CARREGAMENTO(POSTO_ID)
);

Create Table VIAGEM (
  VIAGEM_ID integer primary key,
  USUARIO_ID integer,
  ORIGEM VARCHAR(50),
  DESTINO varchar(255),
  AUTONOMIA_RESTANTE int,
  DATA_AVALIACAO date,
  foreign key (USUARIO_ID) references USUARIO(USUARIO_ID)
);

Create Table RESERVA (
  RESERVA_ID integer primary key,
  USUARIO_ID integer,
  PONTO_ID integer,
  DATA_RESERVA date,
  STATUS varchar(50),
  foreign key (USUARIO_ID) references USUARIO(USUARIO_ID),
  foreign key (PONTO_ID) references PONTO_CARREGAMENTO(PONTO_ID)
);

Create Table PONTO_PARADA (
  PONTO_PARADA_ID integer primary key,
  VIAGEM_ID integer,
  PONTO_ID integer,
  ORDEM integer,
  foreign key (VIAGEM_ID) references VIAGEM(VIAGEM_ID),
  foreign key (PONTO_ID) references PONTO_CARREGAMENTO(PONTO_ID)
);

Create Table HISTORICO_CARREGAMENTO (
  HISTORICO_ID integer primary key,
  USUARIO_ID integer,
  PONTO_ID integer,
  DATA_CARREGAMENTO date,
  ENERGIA_CONSUMIDA decimal(10, 2),
  EMISSOES_EVITADAS decimal(10, 2),
  foreign key (USUARIO_ID) references USUARIO(USUARIO_ID),
  foreign key (PONTO_ID) references PONTO_CARREGAMENTO(PONTO_ID)
);

Create Table AVALIACAO (
  AVALIACAO_ID integer primary key,
  USUARIO_ID integer,
  POSTO_ID integer,
  NOTA integer,
  COMENTARIO varchar(255),
  DATA_AVALIACAO date,
  foreign key (USUARIO_ID) references USUARIO(USUARIO_ID),
  foreign key (POSTO_ID) references POSTO_CARREGAMENTO(POSTO_ID)
);

CREATE SEQUENCE USUARIO_SEQ START WITH 22 INCREMENT BY 50;
CREATE SEQUENCE VIAGEM_SEQ START WITH 22 INCREMENT BY 50;
CREATE SEQUENCE VEICULO_SEQ START WITH 22 INCREMENT BY 50;
CREATE SEQUENCE RESERVA_SEQ START WITH 22 INCREMENT BY 50;
CREATE SEQUENCE POSTO_CARREGAMENTO_SEQ START WITH 22 INCREMENT BY 50;
CREATE SEQUENCE PONTO_CARREGAMENTO_SEQ START WITH 22 INCREMENT BY 50;
CREATE SEQUENCE PONTO_PARADA_SEQ START WITH 22 INCREMENT BY 50;
CREATE SEQUENCE HISTORICO_CARREGAMENTO_SEQ START WITH 22 INCREMENT BY 50;
CREATE SEQUENCE AVALIACAO_SEQ START WITH 22 INCREMENT BY 50;



